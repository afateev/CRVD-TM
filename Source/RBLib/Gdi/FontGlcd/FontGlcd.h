	template<class DeviceContext>
	class FontGlcd : public Font<DeviceContext>
	{
	public:
		// Дампы шрифтов
		static const unsigned char Terminal5x12[];
		static const unsigned char Terminal10x17[];
		static const unsigned char Arial16x16[];
		static const unsigned char Arial15x16Cyr[];
		static const unsigned char Arial13x16[];
		static const unsigned char Arial12x16Cyr[];
		static const unsigned char Arial10x14[];
		static const unsigned char Arial11x13Cyr[];
		static const int UnavailableCharWidth = 8;
	public:
		#pragma pack(push, 1)
		// Структура заголовка шрифта
		struct FontHdrStruct
		{
			unsigned char fontId;				// Пользовательский идентификатор шрифта (не имеет значения)
			unsigned char Reserved0;
			unsigned short FirstCharacterId;	// Код первого символа в шрифте
			unsigned short LastCharacterId;		// Код последнего символа в шрифте
			unsigned char Height;				// Высота символов шрифта
			unsigned char Reserved1;
		};
		// Структура записи таблицы символов
		struct CharTableStruct
		{
			unsigned int Width	: 8;	// Ширина символа
			unsigned int Offset	: 24;	// Смещения данных символа относительно начала дампа шрифта
		};
		#pragma pack (pop)
	protected:
		const unsigned char *const _fontDump;			// Указатель на дамп шрифта
		const unsigned char *const _fontDumpCyr;		// Указатель на дамп шрифта с кириллицей
	protected:
		FontGlcd();
	public:
		// Конструктор по дампу шрифта (один из дампов объявленных в классе либо дамп хранящийся где-то снаружи)
		FontGlcd(const unsigned char *const fontDump, const unsigned char *const fontDumpCyr = 0) :	_fontDump(fontDump), _fontDumpCyr(fontDumpCyr)
		{

		}

		// Нарисовать символ
		// dc - контекст рисования
		// chid - код символа
		// left - x - координата левого верхнего угла символа
		// top - y - координата левого верхнего угла символа
		// color  - цвет символа
		// Возвращает ширину нарисованного символа
		virtual int DrawChar(DeviceContext *dc, unsigned int chId, int left, int top, int color)
		{
			const CharTableStruct *charTableItem = GetCharTableItem(chId);
			// если нет такого символа в таблице ресуем заглушку
			if (charTableItem == 0)
			{
				return DrawUnavailableChar(dc, left, top);
			}

			int bytesCountPerLine = charTableItem->Width / 8;
			if (charTableItem->Width % 8 > 0)
			{
				bytesCountPerLine++;
			}

			const unsigned char *charData = &Dump(IsCyrChar(chId))[charTableItem->Offset];
			for (int y = 0; y < Heihght(); y++)
			{
				unsigned int x = 0;
				while (x < charTableItem->Width)
				{
					if (charData[y * bytesCountPerLine + x / 8] >> (x % 8) & 0x01)
					{
						dc->SetPixel(x + left, y + top, color);
					}
					x++;
				}
			}

			return charTableItem->Width;
		}

		// Получить размер символа
		// dc - контекст рисования
		// chid - код символа
		// width - возвращаемая ширина символа
		// heigth - возвращаемая высота символа
		virtual void GetCharSize(DeviceContext *dc, unsigned int chId, int &width, int &heigth)
		{
			heigth = Heihght();
			
			const CharTableStruct *charTableItem = GetCharTableItem(chId);
			
			if (charTableItem == 0)
			{
				// если нет такого символа в таблице используем размер заглушки
				width = UnavailableCharWidth;
			}
			else
			{
				width = charTableItem->Width;
			}
		}
	protected:
		bool IsCyrChar(unsigned int chId)
		{
			return chId >= 0xC0;
		}

		const unsigned char * Dump(bool cyr)
		{
			return cyr ? _fontDumpCyr : _fontDump;
		}

		FontHdrStruct *Header(bool cyr)
		{
			return (FontHdrStruct *)Dump(cyr);
		}

		CharTableStruct *CharTable(bool cyr)
		{
			return (CharTableStruct *)&Dump(cyr)[sizeof(FontHdrStruct)];
		}

		int Heihght()
		{
			return ((FontHdrStruct *)_fontDump)->Height;
		}

		const CharTableStruct * GetCharTableItem(unsigned int chId)
		{
			bool cyr = IsCyrChar(chId);
			
			if (cyr)
			{
				chId += (0x410 - 0xC0);
			}

			if (Header(cyr) == 0)
			{
				return 0;
			}
			
			if (chId < Header(cyr)->FirstCharacterId)
			{
				return 0;
			}

			if (chId > Header(cyr)->LastCharacterId)
			{
				return 0;
			}

			return &CharTable(cyr)[chId - Header(cyr)->FirstCharacterId];
		}

		int DrawUnavailableChar(DeviceContext *dc, int left, int top)
		{
			dc->FrameRect(left, top, left + UnavailableCharWidth, top + Heihght());
			return UnavailableCharWidth;
		}

	public:
		
	};

